import math
A,B = (input("")).split()
Alas = math.sqrt(int(B)*int(B)-int(A)*int(A))
Tinggi = int(A)
Keliling = int(A) + int(B) + int(Alas)
Luas = float(1/2)*int(Alas)*int(A)
print("Alas = ",round(Alas),"cm")
print("Tinggi = ",round(Tinggi),"cm")
print("Keliling = ",round(Keliling),"cm")
print("Luas = ",round(Luas),"cm^2\n")
